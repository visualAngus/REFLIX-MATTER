void esp_secure_cert_crypto_convert_key_to_der_test()
{
    char ecdsa_key_buf[32] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F,
        0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F};
    char *der_ecdsa_key_buf = (char *)malloc(ESP_SECURE_CERT_ECDSA_DER_KEY_SIZE);
    if (der_ecdsa_key_buf == NULL) {
        ESP_LOGE(TAG, "Failed to allocate memory");
        return;
    }
    esp_err_t esp_ret = esp_secure_cert_convert_key_to_der(ecdsa_key_buf, 32, der_ecdsa_key_buf, ESP_SECURE_CERT_ECDSA_DER_KEY_SIZE);
    if (esp_ret != ESP_OK) {
        ESP_LOGE(TAG, "Failed to convert the key to der");
        return;
    }
    int ret = memcmp(expected_der_ecdsa_key_buf, der_ecdsa_key_buf, ESP_SECURE_CERT_ECDSA_DER_KEY_SIZE);
    if (ret != 0) {
        ESP_LOGE(TAG, "Failed to convert the key to der");
        return;
    }
    ESP_LOGI(TAG, "Successfully converted the key to der");
}
